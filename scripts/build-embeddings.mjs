// Pre-compute Gemini embeddings for all drugs + diseases.
// Run locally once (or whenever data changes):
//   GEMINI_API_KEY=xxxx node scripts/build-embeddings.mjs
//
// Output: src/data/embeddings.json — bundled into the Netlify Function.

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath, pathToFileURL } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')

const API_KEY = process.env.GEMINI_API_KEY
if (!API_KEY) {
  console.error('ERROR: set GEMINI_API_KEY env var first.')
  console.error('  Get a free key at https://aistudio.google.com/apikey')
  process.exit(1)
}

const EMBED_MODEL = 'gemini-embedding-001'
const EMBED_DIM = 768
const EMBED_URL = `https://generativelanguage.googleapis.com/v1beta/models/${EMBED_MODEL}:embedContent?key=${API_KEY}`

// Dynamic import the .js data modules (they use ESM `export const`).
// On Windows, absolute paths must be converted to file:// URLs.
const { drugs } = await import(pathToFileURL(resolve(ROOT, 'src/data/drugs.js')).href)
const { diseases } = await import(pathToFileURL(resolve(ROOT, 'src/data/diseases.js')).href)

// Optional TFDA OTC import (generated by `npm run import-tfda`).
// If file missing, we just embed the hand-curated 28 chunks.
let tfdaChunks = []
const TFDA_PATH = resolve(__dirname, 'cache/tfda-otc-drugs.json')
try {
  tfdaChunks = JSON.parse(readFileSync(TFDA_PATH, 'utf8'))
  console.log(`Loaded ${tfdaChunks.length} TFDA OTC chunks from ${TFDA_PATH}`)
} catch {
  console.log(`(no TFDA cache at ${TFDA_PATH}; skipping — run \`npm run import-tfda\` to include)`)
}

// Optional NHI import. Disabled by default to save free-tier quota (3000+ chunks).
// Enable with: INCLUDE_NHI=1 npm run embeddings
// 預設只嵌入「有健保支付價（price > 0）」的品項，跳過未列價格者以控制語料庫
// 大小；設 INCLUDE_NHI_UNPRICED=1 可改為納入全部 NHI 品項。
let nhiChunks = []
if (process.env.INCLUDE_NHI === '1') {
  const NHI_PATH = resolve(__dirname, 'cache/nhi-drugs.json')
  try {
    nhiChunks = JSON.parse(readFileSync(NHI_PATH, 'utf8'))
    if (process.env.INCLUDE_NHI_UNPRICED !== '1') {
      const UI_PATH = resolve(ROOT, 'public/nhi-drugs.json')
      const pricedIds = new Set(
        JSON.parse(readFileSync(UI_PATH, 'utf8'))
          .filter(d => Number(d.price) > 0)
          .map(d => d.id),
      )
      const before = nhiChunks.length
      nhiChunks = nhiChunks.filter(c => pricedIds.has(c.id))
      console.log(`Loaded ${nhiChunks.length} NHI chunks (INCLUDE_NHI=1; filtered to price>0, skipped ${before - nhiChunks.length} unpriced)`)
    } else {
      console.log(`Loaded ${nhiChunks.length} NHI chunks from ${NHI_PATH} (INCLUDE_NHI=1, INCLUDE_NHI_UNPRICED=1)`)
    }
  } catch {
    console.log(`(INCLUDE_NHI=1 but no NHI cache; run \`npm run import-nhi\` first)`)
  }
}

function drugToText(d) {
  return [
    `【藥品】${d.name}（${d.englishName}）`,
    `品牌名：${d.brandName}`,
    `分類：${d.category}｜產地：${d.origin}`,
    `標籤：${d.tags.join('、')}`,
    `用法：${d.usage}`,
    `劑量：${d.dosage}`,
    `作用機轉：${d.mechanism}`,
    `適應症：${d.indications.join('；')}`,
    `副作用：${d.sideEffects.join('；')}`,
    `注意事項：${d.warnings}`,
  ].join('\n')
}

function diseaseToText(d) {
  return [
    `【疾病】${d.name}（${d.englishName}）`,
    `分類：${d.level}`,
    `概述：${d.overview}`,
    `台灣流行狀況：${d.epidemiology}`,
    `症狀：${d.symptoms.join('；')}`,
    `預防：${d.prevention.join('；')}`,
    `治療：${d.treatment}`,
    `何時就醫：${d.whenToSee}`,
  ].join('\n')
}

const chunks = [
  ...drugs.map(d => ({ id: `drug-${d.id}`, type: 'drug', name: d.name, text: drugToText(d) })),
  ...diseases.map(d => ({ id: `disease-${d.id}`, type: 'disease', name: d.name, text: diseaseToText(d) })),
  ...tfdaChunks,
  ...nhiChunks,
]

console.log(`Embedding ${chunks.length} chunks (${drugs.length} drugs + ${diseases.length} diseases + ${tfdaChunks.length} TFDA OTC + ${nhiChunks.length} NHI)...`)

async function embed(text) {
  const res = await fetch(EMBED_URL, {
    method: 'POST',
    headers: { 'content-type': 'application/json' },
    body: JSON.stringify({
      model: `models/${EMBED_MODEL}`,
      content: { parts: [{ text }] },
      taskType: 'RETRIEVAL_DOCUMENT',
      outputDimensionality: EMBED_DIM,
    }),
  })
  if (!res.ok) {
    const body = await res.text()
    throw new Error(`Embedding API ${res.status}: ${body}`)
  }
  const json = await res.json()
  return json.embedding.values
}

// Gemini free tier: 100 RPM for gemini-embedding-001. Min interval ≈ 600ms.
// Use 700ms for safety + retry-on-429 with backoff.
const MIN_INTERVAL_MS = 700

async function embedWithRetry(text, attempt = 0) {
  try {
    return await embed(text)
  } catch (err) {
    const msg = String(err.message ?? err)
    if ((msg.includes(' 429') || msg.includes(' 503')) && attempt < 5) {
      const wait = 2000 * Math.pow(2, attempt)
      console.log(`  (retry in ${wait}ms — ${msg.slice(0, 80)})`)
      await new Promise(r => setTimeout(r, wait))
      return embedWithRetry(text, attempt + 1)
    }
    throw err
  }
}

// Incremental: load existing vectors by (id + text hash) so we only embed new/changed chunks.
const outPath = resolve(ROOT, 'netlify/functions/embeddings.json')
let cached = new Map()
try {
  const prev = JSON.parse(readFileSync(outPath, 'utf8'))
  if (prev.dim === EMBED_DIM && prev.model === EMBED_MODEL) {
    for (const c of prev.chunks) {
      cached.set(`${c.id}::${hash(c.text)}`, c.vector)
    }
    console.log(`Loaded ${cached.size} cached vectors from existing embeddings.json`)
  } else {
    console.log('Existing embeddings.json has different model/dim — rebuilding from scratch')
  }
} catch {
  console.log('No existing embeddings.json — building from scratch')
}

function hash(s) {
  // Small non-crypto hash; cheap stability check.
  let h = 2166136261
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i)
    h = Math.imul(h, 16777619)
  }
  return (h >>> 0).toString(36)
}

function saveSnapshot(rows) {
  if (rows.length === 0) return
  writeFileSync(outPath, JSON.stringify({ model: EMBED_MODEL, dim: rows[0].vector.length, chunks: rows }, null, 0))
}

const out = []
let reused = 0
let embedded = 0
let savedAt = 0

try {
  for (const c of chunks) {
    const key = `${c.id}::${hash(c.text)}`
    const cachedVec = cached.get(key)
    if (cachedVec) {
      out.push({ ...c, vector: cachedVec })
      reused++
      continue
    }
    const i = embedded + 1
    process.stdout.write(`  [embed ${i}] ${c.name.slice(0, 30)} ... `)
    const t0 = Date.now()
    const vector = await embedWithRetry(c.text)
    out.push({ ...c, vector })
    embedded++
    console.log(`ok (${vector.length}d)`)

    // Save every 50 newly-embedded chunks so progress survives crashes / quota errors.
    if (embedded - savedAt >= 50) {
      saveSnapshot(out)
      savedAt = embedded
      console.log(`  (snapshot saved — ${out.length} chunks)`)
    }

    const elapsed = Date.now() - t0
    if (elapsed < MIN_INTERVAL_MS) {
      await new Promise(r => setTimeout(r, MIN_INTERVAL_MS - elapsed))
    }
  }
} catch (err) {
  // On quota/network failure, save what we have so far. Next run resumes via cache.
  console.error(`\nFatal error after ${out.length} chunks (reused ${reused}, embedded ${embedded}):`)
  console.error(`  ${String(err.message ?? err).slice(0, 300)}`)
  saveSnapshot(out)
  console.log(`Saved partial progress to ${outPath} (${out.length} chunks) — re-run later to resume.`)
  process.exit(1)
}

console.log(`\n  reused: ${reused} | newly embedded: ${embedded}`)

writeFileSync(outPath, JSON.stringify({ model: EMBED_MODEL, dim: out[0].vector.length, chunks: out }, null, 0))
console.log(`Wrote ${outPath} (${out.length} chunks, ${out[0].vector.length}d)`)
